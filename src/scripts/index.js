function signIn() {
    var user = document.getElementById("usernameInput").value;
    var password = document.getElementById("passwordInput").value;

    window.alert("Login done with success \nOn username: " + user + "\nAnd password: " + password);
}

function signUp() {
    var user = document.getElementById("usernameInput").value;
    var password = document.getElementById("passwordInput").value;

    window.alert("Register done with success \nOn username: " + user + "\nAnd password: " + password);
}